public class airthmeticoperation {
    public static void main(String[] args) {
        int p=22, q= 40;
        System.out.println(p+q);
        System.out.println(p-q);
        System.out.println(p*q);
        System.out.println(p/q);
        System.out.println(p%q);
//        relation operator
        System.out.println(p!=q);
        System.out.println(p==q);
        System.out.println(p<=q);
        System.out.println(p>=q);
        System.out.println(p<q);

//logical operator
        System.out.println(p<q && p<=q);
        System.out.println(p>q && p<=q);
        System.out.println(p<q || p<=q);

//assignment operator

        System.out.println(p +=q);
    }
}
